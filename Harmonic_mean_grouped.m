function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global i;
global a;
n=str2double(get(handles.edit9,'String'));
l=str2double(get(handles.edit10,'String'));
u=str2double(get(handles.edit11,'String'));
f=str2double(get(handles.edit12,'String'));
if isempty(u)||isnan(u)||~isreal(u)||isempty(l)||isnan(l)||~isreal(l)||isempty(f)||isnan(f)||~isreal(f)||l>u
    uicontrol(handles.edit10);
    set(handles.edit10,'String','');
    set(handles.edit11,'String','');
    set(handles.edit12,'String','');
    h=msgbox('Enter a valid input','ERROR','error');
else
    i=i+1;
    if(i<=n)
        set(handles.uitable1,'ColumnName',{'Lower Limit','Upper Limit','f','x','f/x'});
        a(i,1)=str2double(get(handles.edit10,'String'));
        a(i,2)=str2double(get(handles.edit11,'String'));
        a(i,3)=str2double(get(handles.edit12,'String'));
        set(handles.uitable1,'data',a);
        set(handles.edit10,'String','');
        set(handles.edit11,'String','');
        set(handles.edit12,'String','');
        uicontrol(handles.edit10);
    end
end
if i>=n
    set(handles.edit10,'Enable','Off');
    set(handles.edit11,'Enable','Off');
    set(handles.edit12,'Enable','Off');
    set(handles.uipanel3,'Visible','on');
end
set(handles.uipanel1,'Visible','on');
 
